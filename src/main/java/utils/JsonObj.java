package utils;

public class JsonObj {
    public String UID;
    public String NAME;
    public String AGE;
    public String CID;
    public String TIME;

    public JsonObj(String UID, String NAME, String AGE, String CID, String TIME) {
        this.UID = UID;
        this.NAME = NAME;
        this.AGE = AGE;
        this.CID = CID;
        this.TIME = TIME;
    }
}
